#!/usr/bin/env node
/**
 * Phase 0 RLS cutover runner (docs/phase0-rls-runbook.html).
 *
 * Reads SUPABASE_DB_URL (production session/direct connection string).
 * Never prints the connection string or any key.
 *
 * Subcommands:
 *   characterize  (default) READ-ONLY. Captures pre-state for the 11 scoped
 *                 tables + programs + user_programs, writes a timestamped
 *                 rollback baseline file (DISABLE RLS + recreate prior
 *                 policies verbatim), and prints the editor-lockout pre-flight.
 *   apply         Runs supabase-program-id-migration-t02.sql ->
 *                 supabase-current-program-helper-t04.sql ->
 *                 supabase-program-rls-t03.sql, in order, aborting on first
 *                 error. Each file self-wraps in BEGIN/COMMIT.
 *   verify        READ-ONLY post-state summary.
 *   rollback <f>  Re-applies a baseline file produced by `characterize`.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import pg from 'pg';

const SCRIPTS_DIR = dirname(fileURLToPath(import.meta.url));
const SCOPED_TABLES = [
  'departments', 'academic_years', 'rooms', 'courses', 'faculty',
  'scheduled_courses', 'faculty_preferences', 'scheduling_constraints',
  'release_time', 'pathways', 'pathway_courses'
];
const WATCH_TABLES = [...SCOPED_TABLES, 'programs', 'user_programs'];

function connString() {
  const url = process.env.SUPABASE_DB_URL;
  if (!url) {
    console.error('Missing SUPABASE_DB_URL (production connection string).');
    process.exit(1);
  }
  return url;
}

async function withClient(fn) {
  const client = new pg.Client({
    connectionString: connString(),
    ssl: { rejectUnauthorized: false },
    statement_timeout: 120000
  });
  await client.connect();
  try {
    return await fn(client);
  } finally {
    await client.end();
  }
}

async function snapshotState(client) {
  const rls = await client.query(
    `SELECT c.relname AS table, c.relrowsecurity AS rls_enabled
       FROM pg_class c JOIN pg_namespace n ON n.oid = c.relnamespace
      WHERE n.nspname = 'public' AND c.relname = ANY($1::text[])
      ORDER BY c.relname`, [WATCH_TABLES]);

  const pol = await client.query(
    `SELECT schemaname, tablename, policyname, permissive, roles, cmd, qual, with_check
       FROM pg_policies
      WHERE schemaname = 'public' AND tablename = ANY($1::text[])
      ORDER BY tablename, policyname`, [WATCH_TABLES]);

  const progCol = await client.query(
    `SELECT table_name FROM information_schema.columns
      WHERE table_schema='public' AND column_name='program_id'
        AND table_name = ANY($1::text[]) ORDER BY table_name`, [SCOPED_TABLES]);

  const reg = await client.query(
    `SELECT to_regprocedure('public.current_program()') IS NOT NULL AS has_current_program,
            to_regclass('public.user_programs')        IS NOT NULL AS has_user_programs,
            to_regclass('public.programs')             IS NOT NULL AS has_programs`);

  let userPrograms = { rows: [{ membership_rows: 'n/a', distinct_users: 'n/a' }] };
  if (reg.rows[0].has_user_programs) {
    userPrograms = await client.query(
      `SELECT count(*)::int AS membership_rows,
              count(DISTINCT user_id)::int AS distinct_users
         FROM public.user_programs`);
  }
  const authUsers = await client.query(`SELECT count(*)::int AS n FROM auth.users`);

  return { rls: rls.rows, pol: pol.rows, progCol: progCol.rows.map(r => r.table_name),
           reg: reg.rows[0], userPrograms: userPrograms.rows[0], authUsers: authUsers.rows[0].n };
}

function buildRollbackSql(state) {
  const lines = [
    '-- Phase 0 RLS rollback baseline (auto-generated by phase0-rls-cutover.mjs)',
    `-- captured ${new Date().toISOString()}`,
    '-- Restores prior policies verbatim and the prior RLS-enabled flag.',
    '-- Apply with: node scripts/phase0-rls-cutover.mjs rollback <this-file>',
    'BEGIN;'
  ];
  for (const t of WATCH_TABLES) {
    lines.push(`-- ${t}`);
    lines.push(`DO $$ DECLARE p record; BEGIN`);
    lines.push(`  FOR p IN SELECT policyname FROM pg_policies WHERE schemaname='public' AND tablename='${t}' LOOP`);
    lines.push(`    EXECUTE format('DROP POLICY IF EXISTS %I ON public.${t}', p.policyname); END LOOP; END $$;`);
  }
  for (const r of state.pol) {
    const roles = normalizeRoles(r.roles).join(', ') || 'public';
    const cmd = (r.cmd || 'ALL').toUpperCase();
    let stmt = `CREATE POLICY ${quoteIdent(r.policyname)} ON public.${r.tablename}`;
    stmt += r.permissive === 'PERMISSIVE' ? ' AS PERMISSIVE' : ' AS RESTRICTIVE';
    stmt += ` FOR ${cmd} TO ${roles}`;
    if (r.qual != null) stmt += ` USING (${r.qual})`;
    if (r.with_check != null) stmt += ` WITH CHECK (${r.with_check})`;
    lines.push(stmt + ';');
  }
  for (const r of state.rls) {
    lines.push(`ALTER TABLE public.${r.table} ${r.rls_enabled ? 'ENABLE' : 'DISABLE'} ROW LEVEL SECURITY;`);
  }
  lines.push('COMMIT;', '');
  return lines.join('\n');
}

function quoteIdent(id) { return '"' + String(id).replace(/"/g, '""') + '"'; }

function normalizeRoles(roles) {
  // pg returns name[] as either a JS array OR a Postgres array literal string '{a,b}'.
  if (Array.isArray(roles)) return roles;
  if (roles == null) return [];
  const s = String(roles).trim();
  if (!s || s === '{}') return [];
  if (s.startsWith('{') && s.endsWith('}')) {
    return s.slice(1, -1).split(',').map(x => x.replace(/^"|"$/g, '').trim()).filter(Boolean);
  }
  return [s];
}

function printSummary(title, state) {
  console.log(`\n=== ${title} ===`);
  console.log('RLS enabled per table:');
  for (const r of state.rls) console.log(`  ${r.rls_enabled ? 'ON ' : 'off'}  ${r.table}`);
  const byTable = {};
  for (const p of state.pol) (byTable[p.tablename] ||= []).push(`${p.cmd}->${normalizeRoles(p.roles).join('/')}`);
  console.log(`Policies: ${state.pol.length} total across ${Object.keys(byTable).length} tables`);
  console.log(`program_id present on ${state.progCol.length}/${SCOPED_TABLES.length} scoped tables` +
    (state.progCol.length && state.progCol.length < SCOPED_TABLES.length ? ` (${state.progCol.join(',')})` : ''));
  console.log(`Registry: current_program()=${state.reg.has_current_program}  user_programs=${state.reg.has_user_programs}  programs=${state.reg.has_programs}`);
  console.log(`auth.users=${state.authUsers}  user_programs rows=${state.userPrograms.membership_rows}  distinct members=${state.userPrograms.distinct_users}`);
}

function preflightVerdict(state) {
  console.log('\n=== EDITOR-LOCKOUT PRE-FLIGHT (runbook gap #4) ===');
  const users = state.authUsers;
  const members = state.userPrograms.distinct_users;
  if (users === 0) {
    console.log('No auth.users exist. Editor app is anon-only OR no one is logged in.');
    console.log('-> t03 grants are TO authenticated; confirm whether the editor relies on a logged-in session before applying.');
    return;
  }
  if (members === 'n/a' || state.reg.has_user_programs === false) {
    console.log(`WARNING: ${users} auth user(s) but user_programs table absent (t04 not yet applied).`);
    console.log('-> After t04+t03, every authenticated user needs a user_programs row OR a JWT app_metadata.program_id claim, or the editor returns ZERO rows.');
    return;
  }
  if (Number(members) < Number(users)) {
    console.log(`WARNING: ${users} auth user(s) but only ${members} have user_programs membership.`);
    console.log('-> The unmapped users will get an empty editor after t03 unless they carry a JWT program_id claim. Resolve before applying t03.');
  } else {
    console.log(`OK: all ${users} auth user(s) have user_programs membership. current_program() should resolve post-t03.`);
  }
}

async function runSqlFile(client, file) {
  const sql = readFileSync(join(SCRIPTS_DIR, file), 'utf8');
  process.stdout.write(`\n--- applying ${file} ... `);
  await client.query(sql);
  console.log('OK');
}

async function main() {
  const cmd = process.argv[2] || 'characterize';
  if (cmd === 'characterize') {
    await withClient(async (client) => {
      const state = await snapshotState(client);
      printSummary('PRE-STATE', state);
      preflightVerdict(state);
      const ts = new Date().toISOString().replace(/[:.]/g, '-');
      const out = join(SCRIPTS_DIR, '..', 'docs', `phase0-snapshot-${ts}.sql`);
      writeFileSync(out, buildRollbackSql(state));
      console.log(`\nRollback baseline written: docs/phase0-snapshot-${ts}.sql`);
    });
  } else if (cmd === 'apply') {
    await withClient(async (client) => {
      await runSqlFile(client, 'supabase-program-id-migration-t02.sql');
      await runSqlFile(client, 'supabase-current-program-helper-t04.sql');
      await runSqlFile(client, 'supabase-program-rls-t03.sql');
      const state = await snapshotState(client);
      printSummary('POST-APPLY STATE', state);
    });
  } else if (cmd === 'verify') {
    await withClient(async (client) => printSummary('CURRENT STATE', await snapshotState(client)));
  } else if (cmd === 'rollback') {
    const f = process.argv[3];
    if (!f) { console.error('Usage: rollback <snapshot-file>'); process.exit(1); }
    await withClient(async (client) => {
      const sql = readFileSync(f, 'utf8');
      process.stdout.write(`Re-applying baseline ${f} ... `);
      await client.query(sql);
      console.log('OK');
      printSummary('POST-ROLLBACK STATE', await snapshotState(client));
    });
  } else {
    console.error(`Unknown command: ${cmd}`);
    process.exit(1);
  }
}

main().catch((e) => { console.error('\nFAILED:', e.message); process.exit(1); });
